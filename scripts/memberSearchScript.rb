invalidos = []

# 📊 Procesar CSV
csv = CSV.parse(rawData, headers: true)

csv.each do |row|
  rol = row["rol"]&.strip
  next unless ROLES_PERMITIDOS.include?(rol)

  firstname  = row["firstname"]&.strip
  lastname1  = row["lastname1"]&.strip
  lastname2  = row["lastname2"]&.strip
  org_name   = row["organizacion"]&.strip

  # Verificar si hay un registro con mismos nombres y organización
  repetido = Member.joins(:organization)
                 .where(
                   firstname: firstname,
                   lastname1: lastname1,
                   lastname2: lastname2,
                   organizations: { name: org_name }
                 ).exists?

  if repetido
    repetidos << row.to_h
    next
  end

  # Verificar si los datos básicos son válidos
  datos_completos = firstname.present? && lastname1.present? && lastname2.present?
  organizacion_valida = Organization.exists?(name: org_name)

  if datos_completos && organizacion_valida
    validos << row.to_h
  else
    invalidos << row.to_h
  end
end

